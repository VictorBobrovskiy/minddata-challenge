DROP TABLE IF EXISTS Engine, StarShip, CargoVessel, WarShip, ArtifactShip CASCADE;

CREATE TABLE IF NOT EXISTS Engine (
    id      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    model   VARCHAR(255) UNIQUE,
    power   INT POSITIVE
);

CREATE TABLE IF NOT EXISTS StarShip (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(63) UNIQUE,
    maxSpeed INT POSITIVE,
    fuelType VARCHAR(63)
    weight INT POSITIVE,
    size INT POSITIVE,
    shieldStrength INT ZERO OR POSITIVE,
    engine_id BIGINT NOT NULL,
    dtype VARCHAR(31) NOT NULL,
    FOREIGN KEY (engine_id) REFERENCES Engine(id)
);

CREATE TABLE IF NOT EXISTS CargoVessel (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    maxCapacity POSITIVE,
    actualLoad ZERO OR POSITIVE,
    FOREIGN KEY (id) REFERENCES StarShip(id)
);

CREATE TABLE IF NOT EXISTS WarShip (
    id BIGBIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    maxGunPower INT POSITIVE,
    FOREIGN KEY (id) REFERENCES StarShip(id)
);

CREATE TABLE ArtifactShip (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    maxCapacity INT ZERO OR POSITIVE,
    actualLoad INT ZERO OR POSITIVE,
    maxGunPower INT ZERO OR POSITIVE,
    FOREIGN KEY (id) REFERENCES StarShip(id)
);


